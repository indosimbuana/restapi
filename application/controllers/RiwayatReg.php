<?php
defined('BASEPATH') or exit('No direct script access allowed');

use chriskacerguis\RestServer\RestController;

class Riwayatreg extends RestController
{

    function __construct()
    {
        parent::__construct();
        $cek = $this->token->cek();
        if ($cek['status'] == false) {
            $this->response([
                'status' => $cek['status'],
                'message' => $cek['message']
            ], $cek['code']);
        }
    }

    public function index_get()
    {
        $id = $this->get('kode');
        $ag = $this->get('anggota');

        $this->load->model('mriwayatreg');
        if ($id === null) {
            if ($ag === null || $ag === '') {
                $this->response([
                    'status' => false,
                    'message' => 'ID Akun kosong'
                ], 404);
            } else {
                $a = $this->mriwayatreg->getRiwayatReg($ag);
                if ($a) {
                    $n = 0;
                    foreach ($a as $dt) {
                        $data[$n]['kodebooking'] = trim($dt['kodeBooking']);
                        $data[$n]['idanggotakeluarga'] = trim($dt['idAnggotaKeluarga']);
                        $data[$n]['poli'] = ucwords($dt['namaBagian']);
                        $data[$n]['waktu'] = trim($dt['waktuPemeriksaan']);
                        $data[$n]['tglperiksa'] = date_format(date_create($dt['tglPemeriksaan']), "d-m-Y");
                        $n++;
                    }
                    $this->response([
                        'status' => true,
                        'message' => 'Data found',
                        'data' => $data
                    ], 200);
                } else {
                    $this->response([
                        'status' => false,
                        'message' => 'No booking were found'
                    ], 404);
                }
            }
        } else {
            $da = $this->mriwayatreg->getRiwayatById($id);
            if ($da) {
                $t = new DateTime($da->tglPemeriksaan);

                switch ($t->format('D')) {
                    case "Sun":
                        $hari = "Minggu";
                        break;
                    case "Mon":
                        $hari = "Senin";
                        break;
                    case "Tue":
                        $hari = "Selasa";
                        break;
                    case "Wed":
                        $hari = "Rabu";
                        break;
                    case "Thu":
                        $hari = "Kamis";
                        break;
                    case "Fri":
                        $hari = "Jumat";
                        break;
                    case "Sat":
                        $hari = "Sabtu";
                        break;
                    default:
                        $hari = "";
                }

                $jdwpoli = $this->mriwayatreg->getJamPoli($da->kodeBagian, $da->waktuPemeriksaan, $da->kodeDokter, $hari);

                if ($jdwpoli) {
                    $jp = date_format(date_create($jdwpoli->$hari), "H:i");
                    $ttp = $hari . 'Tutup';
                    $jptutup = date_format(date_create($jdwpoli->$ttp), "H:i");
                }

                $data['kodebooking'] = trim($da->kodeBooking);
                $data['antriandaftar'] = trim($da->noAntrianPendaftaran);
                $data['antrianpoli'] = trim($da->noAntrianKlinik);
                $data['jamdilayani'] = date_format(date_create($da->jamDilayani), "H:i");
                $data['idanggotakeluarga'] = trim($da->idAnggotaKeluarga);
                $data['poli'] = ucwords($da->namaBagian);
                $data['waktu'] = trim($da->waktuPemeriksaan);
                $data['jampoli'] = isset($jp) ? $jp : '';
                $data['jamtutuppoli'] = isset($jptutup) ? $jptutup : '';
                $data['tglperiksa'] = date_format(date_create($da->tglPemeriksaan), "d-m-Y");
                $data['penjamin'] = trim($da->namaPenjamin);
                $data['nopenjamin'] = trim($da->noPenjamin);
                $data['norujukan'] = trim($da->noRujukan);
                $data['dokter'] = ucwords($da->namaDokter);
                $data['tgldaftar'] = date_format(date_create($da->dateEntry), "d-m-Y");
                $this->response([
                    'status' => true,
                    'message' => 'Data found',
                    'data' => $data
                ], 200);
            } else {
                $this->response([
                    'status' => false,
                    'message' => 'No booking were found'
                ], 404);
            }
        }
    }
}
